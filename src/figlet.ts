/**
 * 文字列をアスキーアート(6行)に描画する。
 * 未対応文字は無視される。
 *
 * @example
 *   figlet("HELLO") →
 *     " ██  ██   ██████   ██       ██       ██████ "
 *     " ██  ██   ██       ██       ██      ██  ██ "
 *     " ██████   █████    ██       ██      ██  ██ "
 *     " ██████   █████    ██       ██      ██  ██ "
 *     " ██  ██   ██       ██       ██      ██  ██ "
 *     " ██  ██   ██████   ██████   ██████   ██████ "
 */
export function figlet(text: string): string[] {
  const upper = text.toUpperCase();
  const rows = ["", "", "", "", "", ""];
  for (const ch of upper) {
    const glyph = FONT[ch];
    if (!glyph) continue;
    for (let i = 0; i < 6; i++) {
      rows[i] += glyph[i];
    }
  }
  return rows;
}
